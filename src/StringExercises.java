import java.util.Scanner;

public class StringExercises {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        String str1 = "We don't need no education \r\nWe don't need no thought control";
        System.out.println(str1);
        System.out.println();

        String str2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(str2);
        System.out.println();

        String str3 = "In windows, the main drive is usually C:\\";
        System.out.println(str3);
        System.out.println();

        String str4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(str4);
        System.out.println();
    }
}
